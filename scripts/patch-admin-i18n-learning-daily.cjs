function patchFile(file, locale) {
  const p = path.join(__dirname, "..", "apps", "admin", "messages", file);
  const j = JSON.parse(fs.readFileSync(p, "utf8"));
  j.adminConsole = j.adminConsole ?? {};
  j.adminConsole.learningPaths = LEARNING_PATHS.vi;
  j.adminConsole.competencies = COMPETENCIES.vi;
  j.adminConsole.learningReview = LEARNING_REVIEW.vi;
  j.adminConsole.dailyItems = DAILY_ITEMS.vi;
  fs.writeFileSync(p, JSON.stringify(j, null, 2) + "\n", "utf8");
  console.log("patched", file);
}

